# String operations
str1 = "Hello"
str2 = "World"

concatenated_str = str1 + " " + str2
str_length = len(concatenated_str)
uppercase_str = str1.upper()
lowercase_str = str2.lower()

# Print string operations
print("Concatenation:", concatenated_str)
print("String Length:", str_length)
print("Uppercase:", uppercase_str)
print("Lowercase:", lowercase_str)
